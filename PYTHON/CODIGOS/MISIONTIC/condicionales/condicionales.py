#Hacer un programa que lea un número y determine si es positivo o negativo
#Tenga en cuenta si el usuario digita un cero

num=float(input("Ingrese un número: "))

if num==0:
    print("El número ingresado es cero")
elif num>0:
    print("El número es positivo")
else:
    print("El número es negativo")






